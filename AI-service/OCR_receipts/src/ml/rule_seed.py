def _normalize(s: str) -> str:
    """문자열 공백 제거 및 소문자 변환"""
    return (s or "").strip().lower().replace(" ", "")

def classify(merchant: str | None, items: List[str]) -> Tuple[str, float, List[Tuple[str, float]]]:
    """
    룰 기반 1차 분류기
    returns: (best_category, confidence, topk[(category, score)])
    """
    # 1. 텍스트 정규화 및 통합
    norm_merchant = _normalize(merchant)
    norm_items = [_normalize(item) for item in (items or [])]
    
    # 검색 대상 전체 텍스트 (상호명 + 품목명)
    full_text = norm_merchant + "".join(norm_items)
    
    scores = {c: 0.0 for c in CATEGORIES}

    # =========================================================
    # Rule 1. 식비 강력 분류 (사용자 요청: 위치 상관없이 단어 있으면 식비)
    # =========================================================
    # 식비 키워드가 하나라도 포함되면 강력한 가중치 부여
    for kw in FOOD_KEYWORDS:
        n_kw = _normalize(kw)
        if n_kw in full_text: 
            scores["식비"] += 5.0 
            # 식비 관련 단어가 발견되면 즉시 루프를 멈추지는 않고(다른 카테고리도 볼 수 있음),
            # 하지만 식비 점수를 압도적으로 높임.
    
    def _ends_food(s: str) -> bool:
        return s.endswith("밥") or s.endswith("탕") or s.endswith("국") or s.endswith("면") or s.endswith("찌개")

    if _ends_food(norm_merchant):
        scores["식비"] += 3.0
    for item in norm_items:
        if _ends_food(item):
            scores["식비"] += 2.0

    # =========================================================
    # Rule 2. 일반 키워드 매칭
    # =========================================================
    for cat, keywords in RULES:
        if cat == "식비": continue # 식비는 위에서 처리했으므로 패스

        for kw in keywords:
            n_kw = _normalize(kw)
            if n_kw in full_text:
                scores[cat] += 2.0 # 일반 매칭 점수
                
                # 상호명과 정확히 일치하면 추가 가산점 (예: '다이소')
                if n_kw == norm_merchant:
                    scores[cat] += 3.0

    # 점수 집계 및 정규화
    best_cat = max(scores.items(), key=lambda x: x[1])[0]
    best_score = scores[best_cat]

    if best_score <= 0:
        # 매칭된 룰이 전혀 없으면 '기타'로 분류하되, topk에 식비/생활용품 추천
        topk = [("기타", 0.34), ("식비", 0.33), ("생활용품", 0.33)]
        return "기타", 0.34, topk

    total = sum(scores.values())
    probs = [(c, (scores[c] / total if total > 0 else 0.0)) for c in CATEGORIES]
    probs.sort(key=lambda x: x[1], reverse=True)

    confidence = probs[0][1]
    
    # 룰 기반이므로 매칭되면 신뢰도를 꽤 높게 줘도 됨 (0.8 이상)
    # 다만 여기서는 상대적 비율로 리턴
    final_confidence = min(confidence + 0.3, 0.99) if best_score >= 3.0 else confidence

    return probs[0][0], float(final_confidence), probs[:3]